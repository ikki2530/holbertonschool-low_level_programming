/**
 * op_add - add 2 numbers
 * @a: number 1
 * @b: number 2
 * Return: result of the add of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract 2 numbers
 * @a: number 1
 * @b: number 2
 * Return: result of substract of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies of 2 numbers
 * @a: number 1
 * @b: number 2
 * Return: result of multiply of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 numbers
 * @a: number 1
 * @b: number 2
 * Return: result of divide of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module of 2 numbers
 * @a: number 1
 * @b: number 2
 * Return: result of module a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
